    private void isiTableBarang() {  
        dapatData();
        ctrlComp.removeTable(tblBarang,tblModBarang);
        String SQL = "SELECT * FROM barang WHERE "+cariBy+" LIKE '%"+kriteria+"%';";
        ctrlManData.setDefaultTable(SQL,tblModBarang,3);
    }

    private void isiComboCari(){
        cbCari.addItem("ID Barang");
        cbCari.addItem("Nama Barang");        
        cbCari.addItem("Quantity");
        
    }
    
    private void dapatData(){
         switch(cbCari.getSelectedIndex()){
            case 0 : cariBy = "id_brg"; break;
            case 1 : cariBy = "nama_brg"; break;    
            case 2 : cariBy = "qty"; break;
            default : cariBy = "id_brg";
        }
        kriteria = txtCari.getText().trim();
        
        idBarang=txtIdBarang.getText().trim();
        nama=txtNmBarang.getText().trim().replaceAll("'","''");
        qty = ctrlComp.setNoHP(txtQty.getText().trim());
        
        
    }
    
    
    
    
