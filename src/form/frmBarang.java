private void setAwalForm() {
        txtIdBarang.setText("");
        txtNmBarang.setText("");
        txtQty.setText("");
        
        
        btnSimpan.setEnabled(true);
        btnUbah.setEnabled(false);
        btnHapus.setEnabled(false);
        
        isiTableBarang();
    }
    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        dapatData();
        
        dataBarang = new Object []{idBarang, nama, qty};
        
        if(ctrlComp.cekIsiField(dataBarang)){
                       
                if(ctrlAlert.KONFIRMASI(idBarang, "HAPUS")){
                    String SQL = "DELETE FROM barang WHERE id_brg ='"+idBarang+"';";
                    if(ctrlManData.EXECUTE_QUERY(SQL,idBarang,"HAPUS", true)){
                        setAwalForm();
                    }else{
                        setAwalForm();
                    }
                }    
            
        }else{
            ctrlAlert.getDataKosongMessage();
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        dapatData();
        
        dataBarang = new Object []{ idBarang, nama, qty};
        
        if(ctrlComp.cekIsiField(dataBarang)){
       
                if(ctrlAlert.KONFIRMASI(idBarang, "UBAH")){
                    String SQL = "UPDATE barang SET nama_brg='"+nama+"', qty='"+qty+"' WHERE id_brg='"+idBarang+"';";
                    if(ctrlManData.EXECUTE_QUERY(SQL,idBarang,"UBAH", true)){
                        setAwalForm();
                    }else{
                        setAwalForm();
                    }
                }    
            
        }else{
            ctrlAlert.getDataKosongMessage();
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        dapatData();
        
        dataBarang = new Object []{ idBarang, nama, qty};
        
        if(ctrlComp.cekIsiField(dataBarang)){
            
                if(ctrlAlert.KONFIRMASI(idBarang, "SIMPAN")){
                    String SQL = "INSERT INTO barang VALUES('"+idBarang+"','"+nama+"','"+qty+"');";
                    if(ctrlManData.EXECUTE_QUERY(SQL,idBarang,"SIMPAN", true)){
                        setAwalForm();
                    }else{
                        setAwalForm();
                    }
                }    
            
        }else{
            ctrlAlert.getDataKosongMessage();
        }
    }//GEN-LAST:event_btnSimpanActionPerformed
     
