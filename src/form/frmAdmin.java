    private void isiTableAdmin() {  
        dapatData();
        ctrlComp.removeTable(tblAdmin,tblModAdmin);
        String SQL = "SELECT id_admin, username, nm_admin, '************', alamat, no_hp " +
                     "FROM admin WHERE "+cariBy+" LIKE '%"+kriteria+"%';";
        ctrlManData.setDefaultTable(SQL,tblModAdmin,6);
    }

    private void isiComboCari(){
        cbCari.addItem("ID Administrator");
        cbCari.addItem("Username");
        cbCari.addItem("Nama Administrator");        
        cbCari.addItem("Alamat");
        cbCari.addItem("Nomer HP");
        
    }
    
    private void dapatData(){
         switch(cbCari.getSelectedIndex()){
            case 0 : cariBy = "id_admin"; break;
            case 1 : cariBy = "username"; break;    
            case 2 : cariBy = "nm_admin"; break;
            case 3 : cariBy = "password"; break;
            case 4 : cariBy = "alamat"; break;
            case 5 : cariBy = "nohp"; break;
            default : cariBy = "id_admin";
        }
        kriteria = txtCari.getText().trim();
        
        idAdmin=txtIdAdmin.getText().trim();
        user=txtUser.getText().trim().replaceAll("'","''"); 
        nama=txtNamaAdmin.getText().trim().replaceAll("'","''");         
        pass=String.valueOf(txtPass.getPassword()).replaceAll("'","''");         
        alamat = txtAlamat.getText().trim();
        noHp = ctrlComp.setNoHP(txtNoHp.getText().trim());
        
        
    }
    
    
    
    
