    private void tblAdminMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAdminMouseClicked
        row = tblAdmin.getSelectedRow();
        if(evt.getClickCount()==2){
            txtIdAdmin.setText(tblAdmin.getValueAt(row,0).toString());
            txtUser.setText(tblAdmin.getValueAt(row,1).toString());
            txtNamaAdmin.setText(tblAdmin.getValueAt(row,2).toString());           
            txtPass.setText(tblAdmin.getValueAt(row,3).toString());
                if(ctrlValid.cekAdmin(tblAdmin.getValueAt(row,3).toString())){
                    txtPass.setText(ctrlValid.getPasswordAdmin());
                        }            
            txtAlamat.setText(tblAdmin.getValueAt(row,4).toString());
            txtNoHp.setText(tblAdmin.getValueAt(row,5).toString());
                       
            btnSimpan.setEnabled(false);
            btnUbah.setEnabled(true);
            btnHapus.setEnabled(true);
            
            tabPane.setSelectedIndex(0);
        }
    }//GEN-LAST:event_tblAdminMouseClicked

